    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String email = txtEmail.getText().trim();
        
        if(email.isEmpty()){
            JOptionPane.showMessageDialog(this,"Digite seu e-mail para recuperar a senha!","Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        usuarioEncontrado = null;
        for(Usuario u: todosUsuarios){
            if(u.getEmail().equalsIgnoreCase(email)){
                usuarioEncontrado = u;
                break;
            }
        }
        if(usuarioEncontrado == null){
            JOptionPane.showMessageDialog(this,"E-mail não encontrado!\nVerifique se escreveu corretamente","Usuário não encontrado",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        jSeparator2.setVisible(true);
        lblUsuario.setText("Usuário encontrado: " + usuarioEncontrado.getNomeCompleto());
        lblUsuario.setVisible(true);
        lblNovaSenha.setVisible(true);
        txtNovaSenha.setVisible(true);
        lblConfirmarSenha.setVisible(true);
        txtConfirmarSenha.setVisible(true);
        btnRedefinir.setVisible(true);
        btnVoltar.setVisible(true);
        
        pack();
        setLocationRelativeTo(null);
        
        txtNovaSenha.requestFocus();
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnRedefinirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRedefinirActionPerformed
        // TODO add your handling code here:
        String novaSenha = new String(txtNovaSenha.getPassword());
        String confirmarSenha = new String(txtConfirmarSenha.getPassword());
        
        if(novaSenha.isEmpty() || confirmarSenha.isEmpty()){
            JOptionPane.showMessageDialog(this,"Preencha todos os campos!","Aviso",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(!novaSenha.equals(confirmarSenha)){
            JOptionPane.showMessageDialog(this,"As senhas não conferem!","Aviso",JOptionPane.WARNING_MESSAGE);
            txtNovaSenha.setText("");
            txtConfirmarSenha.setText("");
            txtNovaSenha.requestFocus();
            return;
        }
        if(novaSenha.length() < 8){
            JOptionPane.showMessageDialog(this,"Nova senha deve conter no mínimo 8 caracteres","Erro",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            atualizarSenha(usuarioEncontrado,novaSenha);
            JOptionPane.showMessageDialog(this,"Senha redefinida com sucesso","Aviso", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Erro ao redefinir senha","Erro",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRedefinirActionPerformed

