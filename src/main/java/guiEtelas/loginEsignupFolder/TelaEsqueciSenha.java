    private void atualizarSenha(Usuario usuario, String novaSenha)throws Copa2026Exceptions{
        List<Usuario> usuarios = usuarioDAO.carregar();
        boolean encontrado = false;
        
        for(Usuario u: usuarios){
            if(u.getEmail().equals(usuario.getEmail())){
                u.setSenha(novaSenha);
                encontrado = true;
                break;
            }
        }
        if(!encontrado){
            throw new Copa2026Exceptions("Usuário não encontrado!");
        }
        usuarioDAO.salvar(usuarios);
        
        if(usuario.getPersonagem() == Usuario.Tipo.ARBITRO){
            List<Arbitro> arbitros = arbitroDAO.carregar();
            boolean encontradoArbitro = false;
            
            for(Arbitro a: arbitros){
                if(a.getEmail().equals(usuario.getEmail())){
                    a.setSenha(novaSenha);
                    encontradoArbitro = true;
                    break;
                }
            }
            if(!encontradoArbitro){
                Arbitro novoArbitro = new Arbitro(usuario.getNomeCompleto(),
                        usuario.getEmail(),novaSenha,Usuario.Tipo.ARBITRO
                );
                arbitros.add(novoArbitro);
            }
            arbitroDAO.salvar(arbitros);
        }
    }
    
